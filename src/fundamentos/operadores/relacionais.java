package fundamentos.operadores;

public class relacionais {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;

        System.out.println("a = 5, b = 10");
        System.out.println("a == b? " + (a == b));
        System.out.println("a >= b? " + (a >= b));
        System.out.println("a <= b? " + (a <= b));
        System.out.println("a != b? " + (a != b));

    }
}
